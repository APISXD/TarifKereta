class KartuKereta:
    def __init__(self, saldo):
        self.saldo = saldo

    def cek_saldo(self):
        return self.saldo

    def masuk_krl(self):
        if self.saldo >= 5000:
            return "Akses KRL diizinkan"
        else:
            return "Saldo tidak mencukupi untuk masuk KRL"

    def masuk_mrt(self, stasiun_awal, stasiun_akhir):
        if stasiun_awal in tarif_mrt and stasiun_akhir in tarif_mrt[stasiun_awal]:
            tarif = tarif_mrt[stasiun_awal][stasiun_akhir]
            if self.saldo >= tarif and self.saldo >= 15000:  # Check both minimum fare and actual fare
                self.saldo -= tarif
                return f"Akses MRT diizinkan dengan tarif {tarif}"
            elif self.saldo < 15000:
                return "Saldo tidak mencukupi untuk masuk MRT (minimal 15000)"
            else:
                return "Saldo tidak mencukupi untuk masuk MRT"
        else:
            return "Rute tidak valid"

def pilih_kereta(kartu):
    print("Pilih jenis kereta:")
    print("1. KRL")
    print("2. MRT")
    pilihan = input("Masukkan pilihan Anda (1 atau 2): ")

    if pilihan == "1":
        hasil = kartu.masuk_krl()
        if hasil == "Akses KRL diizinkan":
            kartu.saldo -= 5000  # Potong saldo untuk KRL
            print("Saldo setelah masuk KRL: ", kartu.cek_saldo())
            lanjut = input("Ingin melanjutkan dengan MRT? (y/n): ")
            if lanjut.lower() == "y":
                stasiun_awal = input("Masukkan stasiun awal MRT: ")
                stasiun_akhir = input("Masukkan stasiun akhir MRT: ")
                hasil = kartu.masuk_mrt(stasiun_awal, stasiun_akhir)
    elif pilihan == "2":
        stasiun_awal = input("Masukkan stasiun awal: ")
        stasiun_akhir = input("Masukkan stasiun akhir: ")
        hasil = kartu.masuk_mrt(stasiun_awal, stasiun_akhir)
    else:
        hasil = "Pilihan tidak valid"

    return hasil

